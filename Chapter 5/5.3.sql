-- 5.3
--  Exponents, Roots, and Factorial

SELECT 3 ^ 4; -- This expression calculates 3 raised to the power of 4. 3×3×3×3=81
SELECT |/ 10; -- This expression calculates the square root of 10.
SELECT sqrt(10); -- This expression also calculates the square root of 10.
SELECT ||/ 10; -- his expression calculates the cube root of 10. 
SELECT factorial(4); -- This expression calculates the factorial of 4. 4!=4×3×2×1=24 